package com.romanenco.cfrm.grammar;

public class AbstractSymbol implements Symbol {

    private final String name;

    public AbstractSymbol(String name) {
        this.name = name;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public int hashCode() {
        return name.hashCode();
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == this) {
            return true;
        }
        if (obj instanceof Symbol) {
            final AbstractSymbol symbol = (AbstractSymbol)obj;
            return name.equals(symbol.name);
        }
        return false;
    }

    @Override
    public String toString() {
        return name;
    }

}
